public class StdinStdout {

    public static void main(String[] args) {
        scanner1();
    }

    private static void scanner1() {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        System.out.println(a);
        int b = sc.nextInt();
        System.out.println(b);
        int c = sc.nextInt();
        System.out.println(c);
    }


}
